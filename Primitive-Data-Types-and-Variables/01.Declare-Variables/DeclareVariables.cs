using System;

class DeclareVariables
{
    static void Main()
    {
        sbyte number1 = -115;
        byte number2 = 97;
        short number3 = -10000;
        ushort number4 = 52130;
        int number5 = 4825932;

        Console.WriteLine("{0}, {1}, {2}, {3}, {4}", number1, number2, number3, number4, number5);
    }
}

